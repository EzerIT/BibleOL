<h1>The Exam for OTST552 students</h1>

<p>The BibleOL-based Hebrew Final Exam contains the following four elements:</p>

<ul>
	<li><strong>Vocabulary</strong>: Students will be asked 20 words. Students need to provide glosses they have learned as part of their vocab training. Students must use the AUP based vocab builder to make sure their learned glosses match the ones in the BibleOL (a simple pdf version will be provided until AUP has not published the vocab builder).</li>
	<li><strong>Verbal Morphology</strong>: Students must answer 20 verbal forms (covering all tenses, all stems, all verbal classes, suffixed and non-suffixed)</li>
	<li><strong>Phrase analysis</strong>: Students must translate 10 clauses by identifying the phrase function of the different phrases in simple sentences. BibleOL will provide the meaning of unknown words if they do appear.</li>
	<li><strong>Clause relation analysis</strong>: Student must translate ~5 sentences by identifying clause relations (independent vs. dependent clause [relative clause, object clause)</li>
</ul>

<p>Students get access each BibleOL based exercise that will be used in the final exam. Access will be given to them from the moment they enroll into the Hebrew course. Consequently, students can exercise with the materials and get a realistic sense of the final long before the final. If a student&rsquo;s performance is on a top level when doing the exercises, the student will know that she or he will perform well in the final new. No surprises should be expected. This is because the data pool for the exam-prep exercises are identical to the actual exam.</p>

