<h1>TOHFL-I Final Exam</h1>

<p>The TOHFL-I Final Exam consists of the following eight elements:</p>

<ul>
	<li><strong>Vocabulary</strong>: Students will be asked 20 words. Students need to provide glosses they have learned as part of their vocab training. Students must use the TOHFL-I vocab booklet to make sure their learned glosses match the ones in the BibleOL.</li>
	<li><strong>Morphology Prefixing</strong>: Students must choose the right vowel+consonant combinations for prefixing prepositions and conjunctions to words. 10 words need to be prefixed correctly.</li>
	<li><strong>Morphology Nouns</strong>: Students must analyze 10 nominal forms (covering all states, number, gender).</li>
	<li><strong>Morphology Suffixation</strong>: Students must analyze 5 suffixes attached to nominal forms (person, number, gender).</li>
	<li><strong>Verbal Morphology</strong>: Students must answer 16 verbal forms (covering all tenses, all stems, all verbal classes, suffixed and non-suffixed forms).</li>
	<li><strong>Subphrasre Analysis</strong>: Students must analyze 10 subphrase relations (is the word relation part of a construct relation, attributive-adjective relation, a demonstrative relation, or something else).</li>
	<li><strong>Phrase analysis</strong>: Students must translate 10 clauses by identifying the phrase function of the different phrases in simple sentences. BibleOL will provide the meaning of unknown words if they do appear.</li>
	<li><strong>Clause relation analysis</strong>: Student must translate ~6 sentences by identifying clause relations (independent vs. dependent clause [relative clause, object clause)</li>
</ul>

<p>Students can exercise with the final exam materials and get a realistic sense of the final exam before the actual exam day. If a student&rsquo;s performance is on a top level when doing the exercises, the student will know that she or he will perform well in the final new. No surprises should be expected. This is because the data pool for the exam-prep exercises are identical to the actual exam.</p>

