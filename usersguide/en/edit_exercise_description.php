<h1>Edit Exercise - Description</h1>

<p>In the &ldquo;Description&rdquo; tab you can write information and instructions to the students
who will be running this exercise. The students will see this text above each question of the
exercise.</p>

<p>Some formatting of this text is possible, and if you include hyperlinks, the students can click
on them to open the link in a browser. Note that this text is saved together with the exercise. If
the exercise is intended for use by an international audience, you need to choose the language of
this text carefully.</p>
