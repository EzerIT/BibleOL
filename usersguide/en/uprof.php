<h1>User Profile</h1>

<p>If you are logged in to Bible OL, you can change your account settings or delete your account by
    selecting the menu <i>My Data &gt; Profile</i>.</p>

<p>If you are using an account created directly on Bible OL you will be able to...</p>

<ul>
    <li>Modify your name.</li>
    <li>Modify your email address.</li>
    <li>Specify a preferred interface language. This language will then be automatically selected
        each time you log in.</li>
    <li>Specify a preferred <?= help_anchor('variant','variant') ?>. This variant will then be automatically selected
        each time you log in.</li>
    <li>Modify your password.</li>
</ul>

<p>If you have logged in using a Facebook or Google account, you will be able to...</p>

<ul>
    <li>Modify your name order. (In Chinese the family name comes before the personal name.)</li>
    <li>Specify a preferred interface language. This language will then be automatically selected
        each time you log in.</li>
    <li>Specify a preferred <?= help_anchor('variant','variant') ?>. This variant will then be automatically selected
        each time you log in.</li>
    <li>Modify your password.</li>
</ul>

<h2>Delete Profile</h2>

<p>If you want to delete your profile and your Bible OL account, click the red button labelled
    &rdquo;Delete the profile&ldquo;. This will also delete all data about you on the system,
    including information about how you performed in the exercises. Note: This action cannot be
    undone!</p>
