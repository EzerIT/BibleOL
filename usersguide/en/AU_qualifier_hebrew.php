<h1>Hebrew Placement Exam</h1>

<p>All present or prospective students wanting to meet the Hebrew Language requirements at the SDA Theological Seminary&nbsp; at Andrews University without taking a class can do so by signing up for the Hebrew Placement Exam. The exam is called &ldquo;<strong><em>placement</em></strong>&rdquo; exam because the results will determine whether you have passed all Hebrew language requirements or are&nbsp;<strong><em>placed</em></strong>&nbsp;into an OTST551 (Hebrew I) or OTST552 (Hebrew II) course.</p>

<p>The exam can be taken only&nbsp;<strong><em>once</em></strong>&nbsp;and consists of <u>two parts:</u></p>

<ul>
	<li>In the <u>first part</u>, basic Hebrew-I questions are being asked. The second part is identical to the Hebrew-II Final Exam. One has successfully passed a part when scoring at least 75%. If one has not passed the first part of the exam, one must enroll in an OTST551 course.</li>
	<li>In the <u>second part</u>, basic Hebrew-II questions are being asked. If one has passed the first part but has not passed the second part, one must enroll in an OTST552 course. If one passes both parts successfully, all language requirements are fulfilled, and the student can move on to enrolling in exegesis courses.</li>
</ul>

<p>Below you can see what categories of questions you will have to master in the different parts of the exam. Each category&nbsp;is hyperlinked. When you click on a given hyperlink you can start to exercise and test yourself on each category. The exam will contain exactly the same type of questions and difficulty level you are encountering when using the hyperlinked exercises.</p>

