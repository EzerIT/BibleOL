<p>Students at any location can prepare by using the above links. The links lead to exercises identical to the parts appearing in the Hebrew Qualifier. Each student has 3 hours to complete the exam.</p>

<p>The exam will be made available by the OT department at the Seminary and can be taken at the location of any of our partner schools. Results will be available instantly. The Qualifier can be taken only one time! However, the above links can be used endlessly to make good preparation possible. Instructors can also use the above links in their classes as class material. Please contact otst@andrews.edu to request a Qualifier to be set up for your school. The requesting school should provide the OT department with the date and time they want the Qualifier to be available.</p>
