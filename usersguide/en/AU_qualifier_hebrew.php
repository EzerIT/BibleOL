<h1>Hebrew Placement Exam</h1>

<p>All present or prospective students wanting to meet the Hebrew Language requirements without taking a class can do so by signing up for the Hebrew Placement Exam. The exam is called the &ldquo;<strong><em>placement</em></strong>&rdquo; exam because the results will determine whether you have passed all language requirements or are&nbsp;<strong><em>placed</em></strong>&nbsp;into an OTST551 or OTST552 course. The exam can be taken only&nbsp;<strong><em>once</em></strong>&nbsp;and consists of two parts. In the first part, basic Hebrew-I questions are being asked. The second part is identical to the Hebrew-II Final Exam. One has successfully passed a part when scoring at least 75%. If one has not passed the first part of the exam, one must enroll in an OTST551 course. If one has passed the first part but has not passed the second part, one must enroll in an OTST552 course. If one passes both parts successfully, all language requirements are fulfilled, and the student can move on to enrolling in exegesis courses.</p>

