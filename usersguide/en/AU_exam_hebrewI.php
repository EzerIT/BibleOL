<h1>Required Skills</h1>

<p>Andrews University requires from OTST551 students that the following material be mastered:</p>

<ul>
	<li>The entire content of the Page Kelley textbook (chapter I &ndash; XXI).</li>
	<li>All vocab with an occurrence of 200 and higher.</li>
	<li>Basic syntax skills.</li>
</ul>

