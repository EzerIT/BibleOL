<h1>The Exam for OTST551 students</h1>

<p>The BibleOL-based Hebrew-I Final Exam contains the following elements:</p>

<ul>
	<li><strong>Vocabulary</strong>: Students will be asked 20 words. Students need to provide glosses they have learned as part of their vocab training. Students must use the AUP-based vocab builder to make sure their learned glosses match the ones in the BibleOL (a simplified pdf version will be provided until AUP has published its vocab builder).</li>
	<li><strong>Morphology of Prefixing</strong>: Students must choose the right vowel+consonant combinations for prefixing prepositions and conjunctions to words. 10 words must be prefixed correctly.</li>
	<li><strong>Morphology of Nouns</strong>: Students must analyze 10 nominal forms (covering all states, number, gender).</li>
	<li><strong>Morphology of Suffixation</strong>: Students must analyze 5 suffixes (person, number, gender) attached to nominal forms or prepositions.</li>
	<li><strong>Morphology of Verbs</strong>: Students must answer 16 regular verbal forms (covering all tenses, all stems, suffixed and non-suffixed forms).</li>
	<li><strong>Subphrase analysis</strong>: Students must analyze 10 subphrase relations (is the word relation part of a construct relation, attributive-adjective relation, a demonstrative relation, or something else).</li>
	<li><strong>Phrase analysis</strong>: Students must translate 10 clauses by identifying the phrase function of the different phrases in simple sentences. BibleOL will provide the meaning of unknown words if they do appear.</li>
	<li><strong>Clause-relation analysis</strong>: Student must translate ~6 sentences by identifying clause relations (independent vs. dependent clause [relative clause, object clause]).</li>
</ul>

<p>Students get access to each BibleOL-based exercise used in the final exam before taking the exam. Access will be given to them when they enroll in a Hebrew-I course. Consequently, students can exercise with these materials and get a realistic sense of the final exam before the actual exam day. If a student performs at a top level when doing the exercises, the student will know that she or he will perform well in the final exam. No surprises should be expected. This is because the data pool for the exam-prep exercises is identical to the actual exam.</p>

