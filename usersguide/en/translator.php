<h1>Introduction for Translators</h1>

<p>Users with <i>translator</i> privileges can add and modify translations of the Bible OL interface.</p>

<p>Three types of translation are available:</p>

<ul>
    <li><?= help_anchor('tr_ifgr/if','Translation of the user interface') ?>.</li>
    <li><?= help_anchor('tr_ifgr/gr','Translation of the grammatical terms') ?> used by the Hebrew,
        Greek, and Latin text databases in the system.</li>
    <li><?= help_anchor('tr_lex','Translation of the Hebrew, Greek, or Latin lexicon') ?>.</li>
</ul>

<p>If you have <i>translator</i> privileges, you can see an overview of the available translations
    by selecting the <?= help_anchor('avail_trans','<i>Administration &gt; Available
    translations</i>') ?> menu.</p>
