<h2>The Hebrew TOHFL course</h2>

<p>This Hebrew TOHFL course was created to represent a standard course teaching all essential aspects of vocabulary, grammar, syntax, and translation skills required by the accrediting institutions (VU, AU, PThU, Nida). Since each institution might use different textbook&rsquo;s this course does not adopt a specific textbook but uses the widely used <em>Biblical Hebrew Reference Grammar</em> with a few more sources:</p>

<ul>
	<li>Merwe, Christo H. van der, and Jacobus A. Naud&eacute;. <em>A Biblical Hebrew Reference Grammar: Second Edition</em>. 2 edition. London Oxford New York New Delhi Sydney: T&amp;T Clark, 2017.</li>
	<li>Glanz, Oliver. <em>Biblical Hebrew Reviewer: A Universal Study Companion</em>. Berrien Springs: Andrews University Press, 2018.</li>
	<li>TOHFL vocab booklet (download vocab booklet for TOHFL course I <a href="https://liveandrews-my.sharepoint.com/:b:/g/personal/glanz_andrews_edu/Ee-ipaRYb5FEiI38tWECZNgBCxXM9bjcTVHS4Mr5jxAkPA?e=CA6vyo" target="_blank">here</a>, and TOHFL course II <a href="https://liveandrews-my.sharepoint.com/:b:/g/personal/glanz_andrews_edu/ER6s75evELBCphq2S8GG3mgBkFrMxroFPx5gzM9MY9pEVQ?e=iWxGx7" target="_blank">here</a>)</li>
</ul>

<p>Since this course does not use a textbook it chooses to follow a <em>text-driven</em> approach in which vocab and grammar are not learned in abstract but in close relation with the texts which&nbsp;are being translated through both TOHFL courses. TOHFL-I will work with Gen 19-20. TOHFL-II will work with Gen 19-20, Ruth 1, Psa 3. The vocab of each course will cover the words that are found in the texts to be translated in the respective courses.</p>

<p>TOHFL-I will cover the basic morphology of the regular verb, vocabulary down to an occurence of 200x, morphology of the noun, and basic syntax.</p>

<p>TOHFL-II will cover all guttural and weak verb classes, vocabulary down to an occurence of 100x, valence, syntax, and basic text-grammar.</p>

<p>Each course consists of lessons. Each lesson consts of a lecture part with provided video lectures and an assignment part. In the assignment part exercises that are based on the BibleOL are made available. Herewith all the BibleOL advantages are present to the user (instant feedback, self-grading, watching one&rsquo;s performance development).&nbsp;</p>

<p>Each course ends with a final exam. Each of&nbsp;the accrediting institutions are responsible for scheduling the exam and for administrating the certification of completion (if a student has passed the final). The material that is being used for the final exam is made available in advance so that students can practive and prepare for the final exam accordingly.</p>

