<h1>TOHFL-II Final Exam</h1>

<p>The TOHFL-II Final Exam consists of the following four&nbsp;elements:</p>

<ul>
	<li><strong>Vocabulary</strong>: Students will be asked 20 words. Students must provide glosses they have learned in their vocab training. Students must use the AUP-based vocab builder to ensure their learned glosses match those in the BibleOL (a simple PDF version will be provided until AUP has published the new vocab builder).</li>
	<li><strong>Verbal Morphology</strong>: Students must answer 20 verbal forms (covering all tenses, all stems, all verbal classes, suffixed and non-suffixed)</li>
	<li><strong>Phrase analysis</strong>: Students must translate 10&nbsp;clauses by identifying the phrase function of the different phrases in simple sentences. BibleOL will provide the meaning of unknown words if they do appear.</li>
	<li><strong>Clause relation analysis</strong>: Student must translate ~5 sentences by identifying clause relations (independent vs. dependent clause [relative clause, object clause]).</li>
</ul>

<p>Students get access to each BibleOL-based exercise used in the final exam. Access will be given to these exercises when they enroll in the OTST552 (Hebrew II)&nbsp;course. Consequently, students can exercise with these materials and get a realistic sense of the final exam before the actual exam day. If a student performs at a top level when doing the exercises, the student will know that she or he will perform well in the final exam. No surprises should be expected. This is because the data pool for the exam-prep exercises is identical to the actual exam.</p>

