<h1>Font Preferences</h1>

<p>If you are logged in to Bible OL, you can specify what fonts the system should use for Hebrew,
    Greek and Latin texts. You&rsquo;ll find the settings under the menu <i>My Data &gt; Font
    preferences</i>.</p>

<p>At the top of the page, you select whether you want to specify the font for Hebrew,
    transliterated Hebrew, Greek, or Latin. Then you have the option to select one of the built-in
    fonts or specify your own font. Furthermore, you can specify the font size and style used in the
    text display.</p>

<p>Click &ldquo;OK&rdquo; at the bottom of the page to save your preferences. Your font choice will be
    used each time you log in to Bible OL.</p>
