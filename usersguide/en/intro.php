<h1>Introduction to Bible Online Learner</h1>

<p>Bible Online Learner (or &ldquo;Bible OL&rdquo; for short) is a web-based computer program that
    supports reading and learning biblical Hebrew and Greek. Latin texts are also available. Bible
    Online Learner provides these features:</p>


<ul>
    <li>Users can read the Old Testament in Hebrew and the New Testament in Greek and Latin.</li>
    <li>Users can see grammatical information about the words and clauses in the text.</li>
    <li>Teachers of Hebrew, Greek, and Latin can create exercises based on the biblical texts.</li>
    <li>Students can drill and test themselves using exercises created by teachers.</li>
    <li>Teachers can keep track of how well students do in the exercises.</li>
    <li>Teachers can generate exams for students.</li>
</ul>

