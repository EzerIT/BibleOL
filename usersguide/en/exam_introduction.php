<h1>Introduction to Exam</h1>

<p>BibleOL offers the option for creating and taking exams. While each instructor can create exams for students, institutions can use the exam feature to build exams that can be used as qualifiers for incoming students or as standardized exams that departments use for their courses.</p>

<ul>
	<li>If you are a student at Andrews University and preparing to take the final exam for the Hebrew II course (OTST552), go to &ldquo;<a href="https://learner.bible/help/show_help/AU_exam_hebrewII">Andrews University Final Hebrew Exam: Hebrew II</a>&rdquo;.</li>
	<li>If you are a student at Andrews University and preparing to take the final exam for the Intermediate Greek course (NTST552), go to &ldquo;<a href="https://learner.bible/help/show_help/AU_exam_greek">Andrews University Final Greek Exam</a>&rdquo;.</li>
	<li>If you are an existing or prospective student at Andrews University, go to the &ldquo;<a href="https://learner.bible/help/show_help/AU_qualifier_hebrew">Andrews University Hebrew Placement Exam</a>&rdquo; if you signed up for the Hebrew Placement Exam.</li>
</ul>
