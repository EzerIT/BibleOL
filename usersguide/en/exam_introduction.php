<h1>Introduction to Exam</h1>

<p>BibleOL offers the options for creating and taking exam. While each instructor can create exams for students, institutions can use the exam feature to build exams that can be used as Qualifiers for incoming students or as standardized exams that all instructors use in their institutional courses.</p>

<ul>
	<li>If you are a student at Andrews University go to the &ldquo;Andrews University Final Hebrew Exam&rdquo; if you are taking the course OTST552.</li>
	<li>If you are a student at Andrews University go to the &ldquo;Andrews University Final Greek Exam&rdquo; if you are taking the course NTST552.</li>
	<li>If you are an exisiting or prospect student at Andrews University go to &ldquo;Andrews University Hebrew Placement Exam&rdquo; if you signed up for the Hebrew Placement Exam.</li>
</ul>
