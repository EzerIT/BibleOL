<h1>Running Exercises</h1>


<p>In this section we will look at how you run an exercise. We will go through a couple of examples
    that you can try out on the system.</p>

<p>NOTE: You need not be logged in to try the code in this example, but you may see some warnings if
    you are not logged in.</p>

<p>Exercises are located in folders in much the same way files on a computer are located in folders.
    You can access the exercises by selecting <i>Exercises</i> from the <i>Text and exercises</i> menu.
    You will then see a list of folders that are found within the top level folder:</p>

