<h1>Download Lexicon</h1>

<p>If you select the <i>Administration &gt; Download lexicon</i> menu, you will be able to download
    a file with translations of lexemes from Hebrew, Greek, or Latin. A downloaded file contains a
    spreadsheet in CSV (Comma Separated Values) format, which can be imported into most spreadsheet
    programs.</p>

<p>It is also possible to upload a spreadsheet with lexicon information, but that must be done by a
    system administrator using the command line interface to Bible Online Learner.</p>
